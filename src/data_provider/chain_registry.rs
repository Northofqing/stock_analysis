use once_cell::sync::Lazy;
use std::collections::HashMap;

static PRIMARY: Lazy<HashMap<&'static str, &'static str>> = Lazy::new(|| {
    let mut m = HashMap::new();
    for (code, chain) in primary_map() {
        // 同一 code 多次出现时, 后写覆盖前写. 注释里标 (主:) 的放最后写.
        m.insert(*code, *chain);
    }
    m
});

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_lookup_known_codes() {
        assert_eq!(lookup("002916"), Some("AI硬件-PCB"));
        assert_eq!(lookup("300308"), Some("AI硬件-光模块"));
        assert_eq!(lookup("600519"), Some("消费-白酒"));
    }

    #[test]
    fn test_lookup_unknown() {
        assert_eq!(lookup("999999"), None);
        assert_eq!(lookup(""), None);
    }

    #[test]
    fn test_count_mapped() {
        // 至少 30 只 (避免维护缩水)
        assert!(PRIMARY.len() >= 30, "chain registry 缩水到 {} 条", PRIMARY.len());
    }

    // 防回归: 注释里标"主"分类的 code, lookup 返回必须跟注释一致
    // (之前 HashMap 后写覆盖前写, 静默错配)
    #[test]
    fn test_duplicate_codes_have_correct_main_chain() {
        // 浪潮信息 000977: 注释标"主: 服务器"
        assert_eq!(lookup("000977"), Some("AI硬件-服务器"));
        // 海光信息 688041: 注释标"主: 设计"
        assert_eq!(lookup("688041"), Some("半导体-设计"));
    }
}
